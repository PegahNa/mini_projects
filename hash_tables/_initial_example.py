## I'm a List
my_list = ["john", "steve", "nicole", "apple"]
print('My_List Value:', my_list[3])

# I'm a List of Dictionaries - a slightly more complex data set
my_list_of_dictionaries = [
    {"name": "John"},
    {"name": "Steven"},
    {"name": "Nicola"},
    {"name": "Berry Bush"}
]
print('My_List_Of_Dictionaries Value:', my_list_of_dictionaries[1]["name"])

# I'm a Hash Table, kind of...
my_hash_table = {
    1: "John",
    56: "Larry",
    100: "Nicole",
    9001: "It's over 9000"
}
print('My_Hash_Table Value:', my_hash_table[1])
